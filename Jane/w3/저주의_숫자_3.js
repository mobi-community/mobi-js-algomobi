const hasThree = (num) => {
  const toStr = num + "";
  const result = [...toStr].filter((el) => el === "3").length;
  return !!result;
};

const solution = (n) => {
  let cnt = 0;
  let num = 0;
  while (cnt < n) {
    while (hasThree(num)) num += hasThree(num);
    if ((num + 1) % 3 === 0) {
      num += 2;
    } else {
      num += 1;
    }
    while (hasThree(num)) num += hasThree(num);
    if (num % 3 === 0) {
      num += 1;
    }
    cnt += 1;
    // console.log("res", cnt, num);
  }
  return num;
};

